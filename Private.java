/**
 * The Private interface defines methods for managing the owner of a private airplane.
 * It provides a contract for getting and setting the owner's name.
 */
public interface Private {

    /**
     * Retrieves the name of the owner of the private airplane.
     */
    String getOwnerName();

    /**
     * Sets the name of the owner of the private airplane.
     */
    void setOwnerName(String Name);
}
